package com.lyz.his.po;

//HRole:系统中的角色表
public class HRole
{
    private Integer id;
    private String roleName;

    public Integer getId()
    {
        return id;
    }

    public void setId(Integer id)
    {
        this.id = id;
    }

    public String getRoleName()
    {
        return roleName;
    }

    public void setRoleName(String roleName)
    {
        this.roleName = roleName;
    }

}
